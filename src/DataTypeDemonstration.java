import java.util.*;
public class DataTypeDemonstration {
    public static void main(String[] args) {
        int a = 10;
        float b = 12.88f;
        char c = 'd';
        boolean d = true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
